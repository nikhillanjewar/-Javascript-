//unary operators
//  a++ = a=a+1(post increment)
//  a-- = a=a-1 (past increment)

let c = 2;
let d = 3;

console.log("c=",c,"&d=",d);
//c = c+1;
c++;
console.log("c=",c); //3

//d = d+1;
d++;
console.log("d=",d); //4


